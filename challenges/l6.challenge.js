// variables
let name = "Chris";
let age = 37;
let enjoysCoding = true;

// preliminary log
console.log(`Name: ${name}`)
console.log(`Age: ${age}`)
console.log(`Enjoys Coding? ${enjoysCoding}`)

// arithmetic operators
age++;
let remainder = age % 5;

// convert age to string
let strAge = String(age);

// am i older than 20?
let olderThan20 = age > 20;

// output to console
console.log(`New Age: ${age}`)
console.log(`Remainder: ${remainder}`)
console.log(`Age as a string: ${strAge}`)
console.log(`Am I older than 20? ${olderThan20}`)
